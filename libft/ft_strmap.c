#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*newstr;
	int		i;

	i = 0;
	if (s == 0)
		return (0);
	newstr = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (newstr == 0)
		return (0);
	while (s[i] != '\0')
	{
		newstr[i] = f(s[i]);
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
